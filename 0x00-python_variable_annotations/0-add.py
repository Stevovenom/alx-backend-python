#!/usr/bin/env python3

"""
This module contains a single function add that takes two float s and return
sum
"""


def add(a: float, b: float) -> float:

    """Returns the sum of two floating-point numbers

    Args:
        a (float): the first argument
        b (float) the second argument

    Returns:
        float: the sum of two numbers.
    """
    return (a + b)
