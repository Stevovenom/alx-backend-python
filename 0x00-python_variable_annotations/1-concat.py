#!/usr/bin/env python3
"""
concat function that takes string str1 and str2 as arguments
"""


def concat(str1: str, str2: str) -> str:
    """
    Args:
        str1 (string): the first string
        str2 (string): the second string

    Returns:
        str: A concatenated string
    """
    return (str1 + str2)
